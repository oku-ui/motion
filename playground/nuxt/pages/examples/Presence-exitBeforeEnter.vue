<script lang="ts" setup>
import { reactive } from 'vue'
import { Motion, Presence } from '@oku-ui/motion'

const state = reactive({ current: 0 })
const next = () => (state.current = state.current + 1)
</script>

<template>
  <div>
    <button @click="next">
      Next
    </button>
    <Presence exit-before-enter>
      <Motion
        :key="state.current"
        :initial="{ opacity: 0 }"
        :keyframes="{
          opacity: 1,
          x: 100,
          transition: { duration: 1 },
        }"
        :exit="{ opacity: 0, x: 0 }"
        :transition="{ duration: 1 }"
        style="width: 200px; background-color: red; height: 200px"
      >
        {{ state.current }}
      </Motion>
    </Presence>
  </div>
</template>
